      private void populatePsychStatusTable() {
          try
          {
         DefaultTableModel dtm = (DefaultTableModel) psychTbl.getModel();
        dtm.setRowCount(0);
        Organization org = null;
        for(Enterprise enter : network.getEnterpriseDirectory().getEnterpriseList()){
            if(enter instanceof DoctorEnterprise){
                e = enter;
            }
        }
        for (Organization organization : e.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof PsychiatristOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            for(WorkRequest request: org.getWorkQueue().getWorkRequestList()) {
            if(request.getSender().equals(userAccount)){
            Object row[] = new Object[5];
            row[0] = request.getRequestID();
            row[1] = request.getWorkMessage();
            row[2] = request.getReceiver();
            row[3] = ((PsychiatristWorkRequest)request).getPsychiatristResult();
            row[4] = request.getWorkStatus();
            dtm.addRow(row);
            }
    }
        }
          }
           catch(Exception e)
        {
             JOptionPane.showMessageDialog(this, "Please try again");
        }
    }
